#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && 'Z' >= str[i])
		{
			str[i] = str[i] + 32;
		}
		str[i] = str[i];
		i++;
	}
	return (str);
}
